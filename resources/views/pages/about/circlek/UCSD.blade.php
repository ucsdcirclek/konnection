    <h4>General Body Meetings</h4>

    <p>UCSD Circle K hold general body meetings every Monday at 8PM (unless there is a holiday) to discuss upcoming
        events, recognize members, and all around just have fun. Meetings are open to everyone. Meeting location
        will be announced either on our facebook page or on the event calender.</p>

    <h4>Key Events</h4>

    <p>UCSD Circle K puts on hosts a <strong>Masquerade Ball</strong> fundraiser annually during Fall Quarter. Two years ago,
        it took place on November 15th on a triple decker yacht. Last year, it raised over $11,000 in proceeds for charity.
        During fall quarter, the club hosts its <strong>New Member Installation</strong>, a dinner reception that welcomes 
        new members into our club.
        UCSD Circle K also participates in many District Events such as <strong>District Convention</strong> (DCON), 
        <strong>Fall Training Conference</strong> (FTC), <strong>District Large Scale Service Project</strong>
